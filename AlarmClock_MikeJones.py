# AlarmClock is a program that takes the day of week (0-6) and tells user
# what time alarm will go off.



day = raw_input ("Enter day of week: ") # input from user

if day == '0': #first conditional statement

    print ("Alarm will go off at 10:00 a.m.") #output result

elif day == '6': #second conditional statement

    print ("Alarm will go off at 10:00 a.m.") #output result

else:

    print ("Alarm will go off at 6:00 a.m.") #final condition and the output results
